letters = 'abcdefghijk'

#splicing
#Print starting from the 2nd character in the string 'abcdefghijk' to the end of the string.
print(letters[2:])
#Print starting from the 3rd character, d, in the string 'abcdefghijk' to the 6th character, f, in the string.
print(letters[3:6])

#reversing the string
print(letters[::-1])
